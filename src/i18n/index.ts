// 共享的语言状态（单例模式）
const savedLang = typeof localStorage !== 'undefined' 
  ? localStorage.getItem('ielts-language') as Locale 
  : null

const currentLang = ref<Locale>(savedLang || 'zh')

const setLocale = (lang: Locale) => {
  currentLang.value = lang
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('ielts-language', lang)
  }
  // 更新 html lang 属性
  if (typeof document !== 'undefined') {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en'
  }
}

const t = (key: string, params?: Record<string, string>): string => {
  const dict = translations[currentLang.value]
  let text = dict[key] || key
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v)
    })
  }
  
  return text
}

// 创建 i18n 组合式函数
export function useI18n(): I18nContext {
  return {
    t,
    currentLang: readonly(currentLang) as DeepReadonly<Ref<Locale>>,
    setLocale
  }
}

// 提供 i18n 上下文
export function provideI18n(): I18nContext {
  const i18n = useI18n()
  provide(i18nKey, i18n)
  return i18n
}

// 注入 i18n 上下文
export function injectI18n(): I18nContext | undefined {
  return inject(i18nKey)
}

// 导出翻译字典供外部使用
export { translations }